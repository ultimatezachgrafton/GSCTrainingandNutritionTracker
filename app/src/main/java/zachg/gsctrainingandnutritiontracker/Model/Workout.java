package zachg.gsctrainingandnutritiontracker.Model;

public class Workout {

    private String mExerciseName;
    private String mReps;
    private int mExerciseWeight;

    private String mExerciseName1;
    private int mReps1;

    public Workout(){
    }

    public String getExerciseName() {
        return mExerciseName1;
    }

    public void setExerciseName(String exerciseName1) {
        mExerciseName1 = exerciseName1;
    }

    public int getReps() {
        return mReps1;
    }

    public void setReps(int reps1) {
        mReps1 = reps1;
    }

    public int getExerciseWeight() {
        return mExerciseWeight;
    }

    public void setExerciseWeight(int exerciseWeight) {
        mExerciseWeight = exerciseWeight;
    }

}